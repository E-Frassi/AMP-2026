# input() exibe uma mensagem na tela para o usuário.
# Também lê o valor digitado e o retorna como texto.
nome=input("Digite seu nome: ")

# print() exibe informações na tela.
# Pode mostrar textos, números e variáveis.
print("67")

# int() converte um valor para o tipo inteiro.
# É usado para trabalhar com números sem casas decimais.
idade=int("61")

# float() converte um valor para o tipo decimal.
# É usado para números com casas decimais.
altura=float("1.75")

# bool() converte um valor para booleano.
# Valores não vazios geralmente resultam em True.
resultado=bool("texto")

# str() converte um valor para texto.
# É útil para transformar números em strings.
numero=str(42)